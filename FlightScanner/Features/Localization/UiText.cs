    public static string T(string key)
    {
        var culture = CultureInfo.CurrentUICulture.TwoLetterISOLanguageName;
        return T(key, culture);
    }

    public static string T(string key, string? culture)
    {
        culture = NormalizeCulture(culture);
        if (Values.TryGetValue(culture, out var localized) && localized.TryGetValue(key, out var value))
        {
            return value;
        }

        return Values["en"].TryGetValue(key, out var fallback) ? fallback : key;
    }

    private static string NormalizeCulture(string? culture)
    {
        return culture?.Equals("fr", StringComparison.OrdinalIgnoreCase) == true
            ? "fr"
            : culture?.Equals("ar", StringComparison.OrdinalIgnoreCase) == true
                ? "ar"
                : "en";
    }
